# Generated by GitHub Copilot
def _build_task_hints(
    row_count: int,
    column_profiles: list[ColumnDiagnostics],
    correlation_to_target: dict[str, float],
) -> list[str]:
    """Generate lightweight optimization hints based on diagnostics summary."""

    hints: list[str] = []
    avg_missing_ratio = mean(profile.missing_ratio for profile in column_profiles) if column_profiles else 0.0
    if avg_missing_ratio > 0.15:
        hints.append("High missing-value ratio detected; prioritize imputation or robust models.")
    elif avg_missing_ratio > 0.05:
        hints.append("Moderate missingness detected; monitor null handling during preprocessing.")

    numeric_count = sum(1 for profile in column_profiles if profile.detected_type == "numeric")
    if numeric_count >= max(3, len(column_profiles) // 2):
        hints.append("Numeric feature coverage is high; tree-based models are likely strong baselines.")

    if row_count < 5000:
        hints.append("Dataset is relatively small; start with simpler models and strong cross-validation.")
    elif row_count > 100000:
        hints.append("Large dataset detected; consider distributed training or incremental pipelines.")

    strong_correlations = [feature for feature, score in correlation_to_target.items() if abs(score) >= 0.7]
    if strong_correlations:
        hints.append("Strong target correlations found; feature selection can improve training efficiency.")

    if not hints:
        hints.append("Dataset quality profile is balanced; proceed with baseline model benchmarking.")

    return hints


