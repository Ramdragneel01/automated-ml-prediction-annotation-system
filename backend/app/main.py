def _request_id_from_request(request: Request) -> str:
    """Resolve request id from state or incoming header."""

    request_id = getattr(request.state, "request_id", None)
    if isinstance(request_id, str) and request_id:
        return request_id
    return request.headers.get("X-Request-ID", "unknown")


def _build_error_payload(
    request: Request,
    code: str,
    message: str,
    details: object | None = None,
) -> dict[str, object]:
    """Build standardized API error payload with request correlation."""

    payload: dict[str, object] = {
        "error": {
            "code": code,
            "message": message,
            "request_id": _request_id_from_request(request),
        }
    }
    if details is not None:
        payload["error"]["details"] = details
    return payload


def _status_to_error_code(status_code: int) -> str:
    """Map HTTP statuses to stable error contract codes."""

    mapping = {
        400: "bad_request",
        401: "unauthorized",
        403: "forbidden",
        404: "not_found",
        409: "conflict",
        413: "payload_too_large",
        415: "unsupported_media_type",
        422: "validation_error",
        429: "rate_limited",
        503: "service_unavailable",
    }
    if status_code >= 500:
        return "internal_error"
    return mapping.get(status_code, "request_failed")


def _security_headers_for_request(request_id: str) -> dict[str, str]:
    """Return baseline security and tracing headers for API responses."""

    headers = {
        "X-Request-ID": request_id,
        "X-Content-Type-Options": "nosniff",
        "X-Frame-Options": "DENY",
        "Referrer-Policy": "strict-origin-when-cross-origin",
        "Permissions-Policy": "camera=(), microphone=(), geolocation=()",
    }
    if settings.app_env == "production":
        headers["Strict-Transport-Security"] = "max-age=31536000; includeSubDomains"
    return headers


def _error_response(
    request: Request,
    status_code: int,
    code: str,
    message: str,
    details: object | None = None,
    extra_headers: dict[str, str] | None = None,
) -> JSONResponse:
    """Build normalized JSON error response with required headers."""

    headers = _security_headers_for_request(_request_id_from_request(request))
    if extra_headers:
        headers.update(extra_headers)
    return JSONResponse(
        status_code=status_code,
        content=_build_error_payload(request, code=code, message=message, details=details),
        headers=headers,
    )


