@app.post("/diagnostics", response_model=DatasetDiagnosticsResponse)
def diagnostics(payload: DatasetDiagnosticsRequest, _: AuthDep = None) -> DatasetDiagnosticsResponse:
    """Analyze dataset shape, missingness, and numeric signals for model planning."""

    DIAGNOSTICS_COUNTER.inc()

    columns = sorted({column for row in payload.rows for column in row.keys()})
    if not columns:
        raise HTTPException(status_code=400, detail="No columns found in dataset rows")

    row_count = len(payload.rows)
    column_profiles: list[ColumnDiagnostics] = []
    missing_by_column: dict[str, int] = {}
    numeric_summary: dict[str, NumericSummary] = {}
    numeric_columns = set()

    for column in columns:
        raw_values = [row.get(column) for row in payload.rows]
        non_missing_values = [value for value in raw_values if not _is_missing(value)]
        missing_count = row_count - len(non_missing_values)
        missing_ratio = missing_count / row_count
        detected_type = _detect_column_type(non_missing_values)
        unique_count = len({json.dumps(value, ensure_ascii=True, default=str) for value in non_missing_values})

        column_profiles.append(
            ColumnDiagnostics(
                name=column,
                detected_type=detected_type,
                missing_count=missing_count,
                missing_ratio=missing_ratio,
                unique_count=unique_count,
            )
        )
        missing_by_column[column] = missing_count

        numeric_values = [_as_numeric(value) for value in non_missing_values]
        filtered_numeric = [value for value in numeric_values if value is not None]
        if detected_type == "numeric" and filtered_numeric:
            numeric_columns.add(column)
            numeric_summary[column] = NumericSummary(
                mean=mean(filtered_numeric),
                min=min(filtered_numeric),
                max=max(filtered_numeric),
                stddev=pstdev(filtered_numeric) if len(filtered_numeric) > 1 else 0.0,
            )

    correlation_to_target: dict[str, float] = {}
    target_column = payload.target_column if payload.target_column in numeric_columns else None
    if target_column:
        for column in sorted(numeric_columns):
            if column == target_column:
                continue

            left_series: list[float] = []
            right_series: list[float] = []
            for row in payload.rows:
                left_value = _as_numeric(row.get(column))
                right_value = _as_numeric(row.get(target_column))
                if left_value is None or right_value is None:
                    continue
                left_series.append(left_value)
                right_series.append(right_value)

            correlation_to_target[column] = _pearson_correlation(left_series, right_series)

    task_hints = _build_task_hints(
        row_count=row_count,
        column_profiles=column_profiles,
        correlation_to_target=correlation_to_target,
    )

    return DatasetDiagnosticsResponse(
        row_count=row_count,
        column_count=len(columns),
        column_profiles=column_profiles,
        missing_by_column=missing_by_column,
        numeric_summary=numeric_summary,
        correlation_to_target=correlation_to_target,
        task_hints=task_hints,
    )


@app.post("/recommendations", response_model=ModelRecommendationResponse)
def recommend_models(payload: ModelRecommendationRequest, _: AuthDep = None) -> ModelRecommendationResponse:
    """Rank candidate model families using diagnostics and historical outcome metadata."""

    RECOMMENDATION_COUNTER.labels(task_type=payload.task_type).inc()

    candidates = _base_model_families(payload.task_type)
    historical_signals = storage.get_model_family_outcomes()

    row_count = payload.diagnostics.row_count
    profile_count = len(payload.diagnostics.column_profiles)
    avg_missing_ratio = (
        mean(profile.missing_ratio for profile in payload.diagnostics.column_profiles) if profile_count else 0.0
    )
    numeric_ratio = (
        sum(1 for profile in payload.diagnostics.column_profiles if profile.detected_type == "numeric") / profile_count
        if profile_count
        else 0.0
    )

    ranked: list[ModelRecommendation] = []
    for index, (family, rationale) in enumerate(candidates):
        score = 0.72 - (index * 0.1)

        if row_count > 50000 and ("XGBoost" in family or "Transformer" in family or "LSTM" in family):
            score += 0.08
        if row_count < 5000 and ("Linear" in family or "Logistic" in family or "RandomForest" in family):
            score += 0.06
        if avg_missing_ratio > 0.1 and "CatBoost" in family:
            score += 0.07
        if numeric_ratio > 0.65 and ("XGBoost" in family or "RandomForest" in family):
            score += 0.05

        if family in historical_signals:
            score += 0.15 * historical_signals[family]

        ranked.append(
            ModelRecommendation(
                model_family=family,
                score=max(0.05, min(0.99, score)),
                rationale=rationale,
            )
        )

    recommendations = sorted(ranked, key=lambda item: item.score, reverse=True)[:5]
    return ModelRecommendationResponse(
        task_type=payload.task_type,
        recommendations=recommendations,
        signal_source="rule-based+historical-telemetry",
    )


