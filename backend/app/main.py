def _as_numeric(value: Any) -> float | None:
    """Convert value to numeric float when safe; return None for non-numeric values."""

    if isinstance(value, bool):
        return None
    if isinstance(value, (int, float)):
        return float(value)
    return None


# Generated by GitHub Copilot
def _detect_column_type(non_missing_values: list[Any]) -> str:
    """Infer a coarse column type for diagnostics and model hint generation."""

    if not non_missing_values:
        return "empty"

    if all(isinstance(item, bool) for item in non_missing_values):
        return "boolean"

    numeric_count = sum(1 for item in non_missing_values if _as_numeric(item) is not None)
    if numeric_count == len(non_missing_values):
        return "numeric"

    string_count = sum(1 for item in non_missing_values if isinstance(item, str))
    if string_count == len(non_missing_values):
        avg_len = mean(len(item) for item in non_missing_values)
        return "text" if avg_len > 32 else "categorical"

    if numeric_count == 0:
        return "categorical"

    return "mixed"


# Generated by GitHub Copilot
def _pearson_correlation(left: list[float], right: list[float]) -> float:
    """Compute Pearson correlation with defensive checks for low-variance series."""

    if len(left) < 2 or len(right) < 2 or len(left) != len(right):
        return 0.0

    left_mean = mean(left)
    right_mean = mean(right)

    numerator = sum((left_value - left_mean) * (right_value - right_mean) for left_value, right_value in zip(left, right))
    left_scale = math.sqrt(sum((left_value - left_mean) ** 2 for left_value in left))
    right_scale = math.sqrt(sum((right_value - right_mean) ** 2 for right_value in right))

    if left_scale == 0 or right_scale == 0:
        return 0.0

    return max(-1.0, min(1.0, numerator / (left_scale * right_scale)))


