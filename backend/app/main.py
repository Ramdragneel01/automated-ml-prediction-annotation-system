# Generated by GitHub Copilot
def _resolve_client_key(request: Request) -> str:
    """Resolve a stable client key using proxy headers and socket fallback."""

    forwarded_for = request.headers.get("X-Forwarded-For", "")
    if forwarded_for:
        first_hop = forwarded_for.split(",")[0].strip()
        if first_hop:
            return first_hop
    if request.client and request.client.host:
        return request.client.host
    return "unknown-client"


@app.middleware("http")
async def request_context_middleware(request: Request, call_next):
    """Attach request id, security headers, and HTTP-level metrics for each request."""

    request_id = request.headers.get("X-Request-ID", str(uuid4()))
    request.state.request_id = request_id
    if request.method in {"POST", "PUT", "PATCH"}:
        content_length_value = request.headers.get("Content-Length")
        if content_length_value is not None:
            try:
                content_length = int(content_length_value)
            except ValueError:
                return _error_response(
                    request,
                    status_code=400,
                    code="bad_request",
                    message="invalid_content_length",
                )

            if content_length > settings.max_request_body_bytes:
                return _error_response(
                    request,
                    status_code=413,
                    code="payload_too_large",
                    message="request_too_large",
                )
        else:
            body = await request.body()
            if len(body) > settings.max_request_body_bytes:
                return _error_response(
                    request,
                    status_code=413,
                    code="payload_too_large",
                    message="request_too_large",
                )

    start = perf_counter()
    response = None
    elapsed = 0.0

    try:
        response = await call_next(request)
    except Exception:  # pragma: no cover
        REQUEST_COUNTER.labels(
            method=request.method,
            endpoint=request.url.path,
            status="500",
        ).inc()
        logger.exception(
            "Unhandled request exception method=%s path=%s request_id=%s",
            request.method,
            request.url.path,
            request_id,
        )
        raise
    finally:
        elapsed = perf_counter() - start
        REQUEST_LATENCY.labels(method=request.method, endpoint=request.url.path).observe(elapsed)

    REQUEST_COUNTER.labels(method=request.method, endpoint=request.url.path, status=str(response.status_code)).inc()

    for header_name, header_value in _security_headers_for_request(request_id).items():
        response.headers[header_name] = header_value

    logger.info(
        "request_complete method=%s path=%s status=%s duration_ms=%.2f request_id=%s",
        request.method,
        request.url.path,
        response.status_code,
        elapsed * 1000,
        request_id,
    )
    return response


