
"""Rate limiting utilities for in-memory and Redis-backed fixed-window quotas."""

from __future__ import annotations

from collections import defaultdict
import threading
import time
from typing import Protocol

try:  # pragma: no cover - optional import path
    from redis import Redis
    from redis.exceptions import RedisError
except ImportError:  # pragma: no cover - optional import path
    Redis = None  # type: ignore[assignment]
    RedisError = Exception  # type: ignore[assignment]


class RateLimiter(Protocol):
    """Contract shared by rate limiter implementations."""

    @property
    def backend_name(self) -> str:
        """Return backend implementation name."""

    @property
    def active_windows(self) -> int | None:
        """Return active key-window count when available."""

    def is_available(self) -> bool:
        """Return health state of the underlying limiter backend."""

    def allow(self, key: str, limit: int) -> bool:
        """Return True when request can proceed under current window quota."""


class InMemoryRateLimiter:
    """Tracks request counts in a fixed time window per client key."""

    def __init__(self, window_seconds: int = 60) -> None:
        """Initialize rate limiter with configurable fixed window size."""

        self._window_seconds = window_seconds
        self._lock = threading.Lock()
        self._windows: dict[str, tuple[float, int]] = defaultdict(lambda: (0.0, 0))
        self._cleanup_interval = 200
        self._request_counter = 0

    # Generated by GitHub Copilot
    @property
    def backend_name(self) -> str:
        """Return backend implementation name for diagnostics output."""

        return "in_memory"

    # Generated by GitHub Copilot
    @property
    def active_windows(self) -> int | None:
        """Return number of tracked client windows for runtime health visibility."""

        with self._lock:
            return len(self._windows)

    # Generated by GitHub Copilot
    def _cleanup_expired_windows(self, now: float) -> None:
        """Drop stale client windows to avoid unbounded memory growth."""

        expiry_seconds = self._window_seconds * 2
        expired_keys = [key for key, (window_start, _) in self._windows.items() if now - window_start >= expiry_seconds]
        for key in expired_keys:
            del self._windows[key]

    # Generated by GitHub Copilot
    def is_available(self) -> bool:
        """Return limiter availability for health checks."""

        return True

    def allow(self, key: str, limit: int) -> bool:
        """Return True when request can proceed under current window quota."""

        now = time.time()
        with self._lock:
            self._request_counter += 1
            if self._request_counter % self._cleanup_interval == 0:
                self._cleanup_expired_windows(now)

            window_start, count = self._windows[key]
            if now - window_start >= self._window_seconds:
                self._windows[key] = (now, 1)
                return True

            if count >= limit:
                return False

            self._windows[key] = (window_start, count + 1)
            return True


class RedisRateLimiter:
    """Distributed fixed-window limiter backed by Redis for multi-instance deployments."""

    def __init__(
        self,
        redis_url: str,
        window_seconds: int = 60,
        prefix: str = "automl:rate-limit",
    ) -> None:
        """Initialize Redis connection and limiter key configuration."""

        if Redis is None:
            raise RuntimeError("redis package is required when MLOPS_REDIS_URL is configured")

        self._window_seconds = window_seconds
        self._prefix = prefix
        self._client = Redis.from_url(
            redis_url,
            decode_responses=True,
            socket_connect_timeout=2,
            socket_timeout=2,
        )

    # Generated by GitHub Copilot
    @property
    def backend_name(self) -> str:
        """Return backend implementation name for diagnostics output."""

        return "redis"

    # Generated by GitHub Copilot
    @property
    def active_windows(self) -> int | None:
        """Return active key-window count when available; Redis does not expose this cheaply."""

        return None

    # Generated by GitHub Copilot
    def is_available(self) -> bool:
        """Check Redis connectivity for startup and health checks."""

        try:
            return bool(self._client.ping())
        except RedisError:
            return False

    # Generated by GitHub Copilot
    def allow(self, key: str, limit: int) -> bool:
        """Apply a distributed fixed-window counter using Redis INCR and EXPIRE."""

        window_slot = int(time.time() // self._window_seconds)
        redis_key = f"{self._prefix}:{window_slot}:{key}"

        try:
            pipeline = self._client.pipeline(transaction=True)
            pipeline.incr(redis_key)
            pipeline.expire(redis_key, self._window_seconds + 2)
            count, _ = pipeline.execute()
            return int(count) <= limit
        except RedisError:
            return False
