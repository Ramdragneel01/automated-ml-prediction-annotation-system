class LogStorage:
    """Facade over storage backends selected by runtime configuration."""

    def __init__(self, db_path: str, database_url: str | None = None) -> None:
        """Initialize SQLite or PostgreSQL backend based on provided settings."""

        if database_url:
            normalized = database_url.lower()
            if normalized.startswith("postgresql://") or normalized.startswith("postgres://"):
                self._backend: StorageBackend = PostgresLogStorage(database_url)
            else:
                raise RuntimeError("MLOPS_DATABASE_URL currently supports only PostgreSQL URLs")
        else:
            self._backend = SQLiteLogStorage(db_path)

    @property
    def db_path(self) -> str:
        """Expose configured backend path or connection descriptor."""

        return self._backend.db_path

    def _connect(self):
        """Expose backend connection for tests and low-level diagnostics."""

        return getattr(self._backend, "_connect")()

    def insert_log(self, payload: InferenceLog) -> int:
        """Persist one inference record and return generated primary key."""

        return self._backend.insert_log(payload)

    def get_logs(self, limit: int, model_name: str | None = None) -> list[InferenceLog]:
        """Fetch most recent logs optionally filtered by model name."""

        return self._backend.get_logs(limit=limit, model_name=model_name)

    def count_logs(self, model_name: str | None = None) -> int:
        """Return total count of logs optionally filtered by model name."""

        return self._backend.count_logs(model_name=model_name)

    def is_available(self) -> bool:
        """Return backend availability state."""

        return self._backend.is_available()

